// Utility functions
export const getTransition = (properties = 'all', duration = 'base') => {
  const durationValue = transitions[duration] || transitions.base;
  return `${properties} ${durationValue}`;
};

export const getColorWithOpacity = (color, opacity) => {
  return `${color}${Math.round(opacity * 255).toString(16).padStart(2, '0')}`;
};

